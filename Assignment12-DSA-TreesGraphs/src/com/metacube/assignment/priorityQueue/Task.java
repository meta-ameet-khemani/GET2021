package com.metacube.assignment.priorityQueue;

/**
 * Task class represents a task in memory with its priority
 * @author ameet.khemani_metacu
 */
public class Task {
	
	public Integer job;
	public Integer priority;
	
	public Task (Integer job, Integer priority) {
		this.job = job;
		this.priority = priority;
	}
	
}
